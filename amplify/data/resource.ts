import { type ClientSchema, a, defineData } from '@aws-amplify/backend';

const schema = a.schema({
  AppRole: a.enum(['member', 'admin']),
  UserType: a.enum(['homeowner', 'service_provider', 'first_time_buyer', 'guest']),
  ProfileType: a.enum(['individual', 'business']),
  PublishStatus: a.enum(['draft', 'pending', 'active', 'inactive', 'archived']),
  PendingStatus: a.enum(['pending', 'linked', 'converted', 'expired']),
  DealType: a.enum(['property_sales', 'long_term_rent', 'short_term_rent', 'service_deal', 'home_service_deal', 'guest_add_ons']),
  BookingType: a.enum(['service', 'deal_purchase', 'short_term_rent', 'property_viewing']),
  BookingStatus: a.enum(['pending', 'paid', 'confirmed', 'completed', 'cancelled']),
  PaymentStatus: a.enum(['pending', 'paid', 'refunded', 'failed']),
  LeadChargeStatus: a.enum(['pending', 'paid', 'disputed', 'waived']),
  ReviewStatus: a.enum(['published', 'hidden', 'flagged']),
  MessageReferenceType: a.enum(['deal', 'service', 'booking', 'offer', 'system']),

  UserProfile: a.model({
    email: a.email().required(),
    full_name: a.string(),
    phone: a.phone(),
    role: a.ref('AppRole'),
    user_type: a.ref('UserType'),
    profile_type: a.ref('ProfileType'),
    is_verified: a.boolean().default(false),
    credits: a.integer().default(0),
    bio: a.string(),
    profile_photo_url: a.string(),
    business_name: a.string(),
    business_phone: a.phone(),
    business_address: a.string(),
    service_types: a.string().array(),
    service_area: a.string(),
    description: a.string(),
    years_experience: a.integer(),
    hourly_rate: a.float(),
    certifications: a.string(),
    sms_phone_number: a.string(),
    sms_consent: a.boolean().default(false),
    sms_consent_date: a.datetime(),
    sms_consent_ip: a.string(),
    sms_opt_in: a.boolean().default(false),
    sms_opt_in_date: a.datetime(),
    sms_opt_in_ip: a.string(),
    has_seen_sms_optin: a.boolean().default(false),
    onboarding_state: a.string(),
    deleted_at: a.datetime(),
  }).authorization(allow => [allow.owner(), allow.group('ADMINS')]),

  PendingUser: a.model({
    email: a.email().required(),
    full_name: a.string(),
    phone: a.phone(),
    user_type: a.ref('UserType').required(),
    linked_user_email: a.email(),
    status: a.ref('PendingStatus'),
    search_criteria: a.json(),
    service_listing: a.json(),
    selected_home: a.json(),
    project: a.json(),
    notes: a.json(),
  }).authorization(allow => [allow.group('ADMINS')]),

  Property: a.model({
    user_email: a.email().required(),
    user_phone: a.phone(),
    address: a.string().required(),
    property_type: a.string(),
    bedrooms: a.float(),
    bathrooms: a.float(),
    sqft: a.float(),
    year_built: a.integer(),
    market_rating: a.float(),
    rebuild_cost_per_sqft: a.float(),
    land_value: a.float(),
    appraised_value: a.float(),
    total_asset_residual_value: a.float(),
    ai_insights: a.json(),
    status: a.ref('PublishStatus'),
  }).authorization(allow => [allow.owner(), allow.group('ADMINS')]),

  PropertyComponent: a.model({
    property_id: a.id().required(),
    user_email: a.email(),
    component_type: a.string().required(),
    manufacturer: a.string(),
    model_number: a.string(),
    serial_number: a.string(),
    install_date: a.date(),
    condition_rating: a.float(),
    photo_urls: a.url().array(),
    component_data: a.json(),
    status: a.ref('PublishStatus'),
  }).authorization(allow => [allow.owner(), allow.group('ADMINS')]),

  MaintenanceTask: a.model({
    property_id: a.id().required(),
    user_email: a.email(),
    project_title: a.string().required(),
    project_description: a.string(),
    project_type: a.string(),
    component_type: a.string(),
    urgency: a.string(),
    preferred_timeline: a.string(),
    budget_range: a.string(),
    photo_urls: a.url().array(),
    status: a.string().default('open'),
    task_data: a.json(),
  }).authorization(allow => [allow.owner(), allow.group('ADMINS')]),

  Report: a.model({
    property_id: a.id().required(),
    user_email: a.email(),
    report_type: a.string().required(),
    report_data: a.json().required(),
    status: a.ref('PublishStatus'),
  }).authorization(allow => [allow.owner(), allow.group('ADMINS')]),

  ServiceListing: a.model({
    expert_email: a.email().required(),
    expert_name: a.string(),
    expert_phone: a.phone(),
    business_name: a.string().required(),
    business_phone: a.phone(),
    business_address: a.string(),
    business_photo_url: a.url(),
    website_url: a.url(),
    google_place_id: a.string(),
    bio: a.string(),
    service_category: a.string(),
    service_types: a.string().array(),
    service_areas: a.string().array(),
    certifications: a.string().array(),
    years_in_business: a.integer(),
    years_experience: a.integer(),
    hourly_rate: a.float(),
    quote_assistant_instructions: a.string(),
    profile_qr_code_url: a.string(),
    price_list: a.json(),
    work_photos: a.json(),
    social_links: a.json(),
    average_rating: a.float().default(0),
    review_count: a.integer().default(0),
    is_verified: a.boolean().default(false),
    status: a.ref('PublishStatus'),
  }).authorization(allow => [allow.publicApiKey().to(['read']), allow.owner(), allow.group('ADMINS')]),

  Deal: a.model({
    user_email: a.email().required(),
    property_id: a.id(),
    service_listing_id: a.id(),
    deal_type: a.ref('DealType').required(),
    title: a.string().required(),
    description: a.string(),
    price: a.float(),
    price_per_night: a.float(),
    minimum_stay: a.integer(),
    original_price: a.float(),
    combo_discount_percent: a.float(),
    location: a.string(),
    latitude: a.float(),
    longitude: a.float(),
    property_type: a.string(),
    service_category: a.string(),
    service_types: a.string().array(),
    photo_urls: a.url().array(),
    contact_phone: a.phone(),
    contact_email: a.email(),
    bedrooms: a.float(),
    bathrooms: a.float(),
    sqft: a.float(),
    is_combo_deal: a.boolean().default(false),
    additional_services: a.json(),
    status: a.ref('PublishStatus'),
  }).authorization(allow => [allow.publicApiKey().to(['read']), allow.owner(), allow.group('ADMINS')]),

  SavedDeal: a.model({
    deal_id: a.id().required(),
    user_email: a.email().required(),
    notes: a.string(),
  }).authorization(allow => [allow.owner(), allow.group('ADMINS')]),

  Booking: a.model({
    deal_id: a.id(),
    service_listing_id: a.id(),
    booking_type: a.ref('BookingType').required(),
    status: a.ref('BookingStatus'),
    payment_status: a.ref('PaymentStatus'),
    renter_email: a.email().required(),
    renter_name: a.string(),
    renter_phone: a.phone(),
    owner_email: a.email().required(),
    property_address: a.string(),
    service_date: a.date(),
    service_time: a.string(),
    service_price: a.float(),
    total_cost: a.float(),
    message: a.string(),
    payment_intent_id: a.string(),
    payment_amount: a.float(),
    qr_code_data: a.string(),
    qr_code_url: a.url(),
    redeemed: a.boolean().default(false),
    participant_owner_ids: a.string().array(),
  }).authorization(allow => [allow.ownersDefinedIn('participant_owner_ids'), allow.group('ADMINS')]),

  Offer: a.model({
    deal_id: a.id().required(),
    buyer_email: a.email().required(),
    buyer_name: a.string(),
    seller_email: a.email().required(),
    offered_price: a.float(),
    message: a.string(),
    status: a.string().default('pending'),
    participant_owner_ids: a.string().array(),
  }).authorization(allow => [allow.ownersDefinedIn('participant_owner_ids'), allow.group('ADMINS')]),

  Insight: a.model({
    title: a.string().required(),
    slug: a.string(),
    summary: a.string(),
    content: a.string(),
    category: a.string(),
    cover_image_url: a.url(),
    seo: a.json(),
    status: a.ref('PublishStatus'),
  }).authorization(allow => [allow.publicApiKey().to(['read']), allow.group('ADMINS')]),

  Message: a.model({
    sender_email: a.email().required(),
    sender_name: a.string(),
    recipient_email: a.email().required(),
    recipient_name: a.string(),
    subject: a.string().required(),
    content: a.string().required(),
    thread_id: a.string(),
    reference_type: a.ref('MessageReferenceType'),
    reference_id: a.string(),
    parent_message_id: a.id(),
    is_read: a.boolean().default(false),
    read_at: a.datetime(),
    participant_owner_ids: a.string().array(),
  }).authorization(allow => [allow.ownersDefinedIn('participant_owner_ids'), allow.group('ADMINS')]),

  Review: a.model({
    service_listing_id: a.id().required(),
    reviewer_email: a.email().required(),
    reviewer_name: a.string(),
    rating: a.float().required(),
    review_text: a.string(),
    helpful_count: a.integer().default(0),
    status: a.ref('ReviewStatus'),
  }).authorization(allow => [allow.publicApiKey().to(['read']), allow.owner().to(['create', 'update', 'delete']), allow.group('ADMINS')]),

  Transaction: a.model({
    property_id: a.id().required(),
    user_email: a.email(),
    transaction_type: a.string().required(),
    category: a.string(),
    amount: a.float().required(),
    date: a.date().required(),
    description: a.string(),
    receipt_url: a.url(),
    payment_method: a.string(),
    payee: a.string(),
    payer: a.string(),
    is_recurring: a.boolean().default(false),
    recurring_frequency: a.string(),
  }).authorization(allow => [allow.owner(), allow.group('ADMINS')]),

  LeadCharge: a.model({
    provider_email: a.email().required(),
    provider_name: a.string(),
    maintenance_task_id: a.id(),
    property_address: a.string(),
    project_title: a.string().required(),
    lead_amount: a.float().required(),
    lead_quality: a.string(),
    status: a.ref('LeadChargeStatus'),
    stripe_invoice_id: a.string(),
    paid_at: a.datetime(),
  }).authorization(allow => [allow.owner(), allow.group('ADMINS')]),

  ProviderSettings: a.model({
    provider_email: a.email().required(),
    billing_email: a.email(),
    payment_terms: a.string(),
    status: a.ref('PublishStatus'),
    lead_fee_per_lead: a.float(),
    total_leads_received: a.integer().default(0),
    billing_profile: a.json(),
  }).authorization(allow => [allow.owner(), allow.group('ADMINS')]),

  Category: a.model({
    type: a.string().required(),
    name: a.string().required(),
    description: a.string(),
    icon: a.string(),
    is_active: a.boolean().default(true),
    sort_order: a.integer(),
  }).authorization(allow => [allow.publicApiKey().to(['read']), allow.group('ADMINS')]),

  Setting: a.model({
    setting_key: a.string().required(),
    setting_value: a.json(),
    description: a.string(),
  }).authorization(allow => [allow.group('ADMINS')]),

  PageMetadata: a.model({
    page_name: a.string().required(),
    meta_title: a.string(),
    meta_description: a.string(),
    meta_keywords: a.string(),
    is_auto_generated: a.boolean().default(false),
    last_generated_date: a.datetime(),
  }).authorization(allow => [allow.publicApiKey().to(['read']), allow.group('ADMINS')]),
});

export type Schema = ClientSchema<typeof schema>;

export const data = defineData({
  schema,
  authorizationModes: {
    defaultAuthorizationMode: 'userPool',
    apiKeyAuthorizationMode: {
      expiresInDays: 30,
    },
  },
});